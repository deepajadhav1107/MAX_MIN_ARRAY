def maximum(a, b, c):
    print(f"Input values: a={a}, b={b}, c={c}")
    result = max(a, b, c)
    print(f"Maximum value is: {result}")
    return result
def minimum(a, b, c):
    print(f"Input values: a={a}, b={b}, c={c}")
    result = min(a, b, c)
    print(f"Minimum value is: {result}")
    return result
if __name__ == "__main__":
    maximum(3, 9, 6)
